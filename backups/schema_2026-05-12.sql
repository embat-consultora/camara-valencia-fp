CREATE OR REPLACE VIEW "public"."vw_feedback_resumen_empresas" WITH ("security_invoker"='on') AS
 SELECT "e"."nombre" AS "empresa",
    "e"."CIF",
    "count"("p"."id") AS "total_practicas",
    "count"("fr"."id") AS "total_respuestas",
        CASE
            WHEN ("count"("p"."id") > 0) THEN "round"(((("count"("fr"."id"))::numeric / ("count"("p"."id"))::numeric) * (100)::numeric), 2)
            ELSE (0)::numeric
        END AS "porcentaje_completado",
    "min"("fr"."created_at") AS "fecha_primer_feedback",
    "max"("fr"."created_at") AS "fecha_ultimo_feedback",
    "count"(
        CASE
            WHEN ("fr"."created_at" > (CURRENT_DATE - '30 days'::interval)) THEN 1
            ELSE NULL::integer
        END) AS "respuestas_ultimo_mes"
   FROM ((("public"."empresas" "e"
     JOIN "public"."practicas_fp" "p" ON ((("e"."CIF")::"text" = ("p"."empresa")::"text")))
     LEFT JOIN "public"."alumnos" "a" ON (("p"."alumno" = "a"."dni")))
     LEFT JOIN "public"."feedback_respuestas" "fr" ON (("p"."id" = "fr"."practica_id")))
  GROUP BY "e"."nombre", "e"."CIF"
  ORDER BY
        CASE
            WHEN ("count"("p"."id") > 0) THEN "round"(((("count"("fr"."id"))::numeric / ("count"("p"."id"))::numeric) * (100)::numeric), 2)
            ELSE (0)::numeric
        END DESC;


