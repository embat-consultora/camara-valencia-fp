CREATE OR REPLACE VIEW "public"."vw_feedback_stats" WITH ("security_invoker"='on') AS
 WITH "base_data" AS (
         SELECT "e"."CIF" AS "empresa_cif",
            "e"."nombre" AS "empresa_nombre",
            "a"."id" AS "alumno_id",
            TRIM(BOTH FROM "concat"("a"."nombre", ' ', "a"."apellido")) AS "alumno_nombre",
            "p"."id" AS "practica_id",
            "p"."created_at" AS "fecha_inicio_practica",
            "fr"."id" AS "respuesta_id",
            "fr"."created_at" AS "fecha_respuesta",
                CASE
                    WHEN ("fr"."id" IS NOT NULL) THEN 1
                    ELSE 0
                END AS "respondido"
           FROM ((("public"."empresas" "e"
             JOIN "public"."practicas_fp" "p" ON ((("e"."CIF")::"text" = ("p"."empresa")::"text")))
             JOIN "public"."alumnos" "a" ON (("p"."alumno" = "a"."dni")))
             LEFT JOIN "public"."feedback_respuestas" "fr" ON (("p"."id" = "fr"."practica_id")))
        )
 SELECT "empresa_nombre",
    "empresa_cif",
    "count"("practica_id") AS "total_alumnos_asignados",
    "sum"("respondido") AS "total_respuestas_recibidas",
    "round"(((("sum"("respondido"))::numeric / (NULLIF("count"("practica_id"), 0))::numeric) * (100)::numeric), 2) AS "porcentaje_completado",
    "min"("fecha_inicio_practica") AS "primera_practica",
    "max"("fecha_respuesta") AS "ultima_respuesta_recibida",
    "count"(
        CASE
            WHEN ("date_trunc"('month'::"text", "fecha_respuesta") = "date_trunc"('month'::"text", (CURRENT_DATE)::timestamp with time zone)) THEN 1
            ELSE NULL::integer
        END) AS "respuestas_mes_actual"
   FROM "base_data"
  GROUP BY "empresa_nombre", "empresa_cif"
  ORDER BY ("round"(((("sum"("respondido"))::numeric / (NULLIF("count"("practica_id"), 0))::numeric) * (100)::numeric), 2)) DESC;


