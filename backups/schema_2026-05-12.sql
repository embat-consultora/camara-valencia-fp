CREATE OR REPLACE VIEW "public"."vw_feedback_detalle_alumnos" WITH ("security_invoker"='on') AS
 SELECT "e"."nombre" AS "empresa",
    ((("a"."nombre")::"text" || ' '::"text") || ("a"."apellido")::"text") AS "alumno",
    "a"."dni" AS "alumno_dni",
    "p"."id" AS "practica_id",
    "p"."created_at" AS "fecha_inicio_practica",
        CASE
            WHEN ("fr"."id" IS NOT NULL) THEN 'Respondido'::"text"
            ELSE 'Pendiente'::"text"
        END AS "estado",
    "fr"."created_at" AS "fecha_respuesta"
   FROM ((("public"."practicas_fp" "p"
     JOIN "public"."empresas" "e" ON ((("p"."empresa")::"text" = ("e"."CIF")::"text")))
     JOIN "public"."alumnos" "a" ON (("p"."alumno" = "a"."dni")))
     LEFT JOIN "public"."feedback_respuestas" "fr" ON (("p"."id" = "fr"."practica_id")))
  ORDER BY "e"."nombre", "p"."created_at" DESC;


