CREATE OR REPLACE VIEW "public"."vw_empresas_ofertas" WITH ("security_invoker"='on') AS
 SELECT "e"."CIF",
    "e"."nombre",
    "e"."telefono",
    "e"."direccion",
    "e"."localidad",
    "e"."codigo_postal",
    "e"."email_empresa",
    "e"."responsable_legal",
    "e"."nif_responsable_legal",
    "e"."horario",
    "e"."pagina_web",
    "e"."nombre_rellena",
    "cf"."key" AS "ciclo_formativo",
    (("cf"."value" ->> 'alumnos'::"text"))::integer AS "alumnos_pedidos",
    (("cf"."value" ->> 'disponibles'::"text"))::integer AS "cupos_disponibles",
    ( SELECT "string_agg"("concat"(("pitem"."value" ->> 'area'::"text"), ' — ', ("pitem"."value" ->> 'proyecto'::"text")), ' | '::"text") AS "string_agg"
           FROM ("jsonb_each"("o"."puestos") "px"("key", "value")
             JOIN LATERAL "jsonb_array_elements"("px"."value") "pitem"("value") ON (true))
          WHERE ("px"."key" = "cf"."key")) AS "areas_puestos",
    "o"."requisitos",
    "o"."contrato",
    "o"."vehiculo",
    "o"."cupo_alumnos" AS "cupo_alumnos_totales_oferta",
    "t"."nombre" AS "tutor_nombre",
    "t"."email" AS "tutor_email",
    "t"."telefono" AS "tutor_telefono"
   FROM ((("public"."empresas" "e"
     JOIN "public"."oferta_fp" "o" ON ((("o"."empresa")::"text" = ("e"."CIF")::"text")))
     LEFT JOIN "public"."tutores" "t" ON (("t"."id" = "o"."tutor")))
     LEFT JOIN LATERAL "jsonb_each"("o"."ciclos_formativos") "cf"("key", "value") ON (true))
  ORDER BY "e"."nombre", "o"."id", "cf"."key";


